package org.usfirst.frc.team3158.robot;


/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	
	//Collector Subsystem
	public static int CollectorLeft;
	public static int CollectorRight;
	public static int CollectorPistonsOn;
	public static int CollectorPistonsOff;
	public static int CollectorLimitSwitch;
	public static double SwitchSpeed;
	public static double ScaleSpeed;
	
	
}
